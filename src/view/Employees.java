    private Employee setEmployee() {
        Employee c = null;
        String name, address, addedDate = null;
        int mobile = 0, phone = 0, nic = 0;
        Verify ver = new Verify();

        name = nameText.getText();
        if (!name.equals("")) {
            nameFlag = true;
        }
        address = addressText.getText();
        if (!address.equals("")) {
            addressFlag = true;
        }
        String m = mobileText.getText();
        if (!m.equals("")) {
            try {
                mobile = Integer.parseInt(m);
                if (ver.verifyMobileNumber(mobileText)) {
                    mobileFlag = true;
                }
            } catch (NumberFormatException e) {
                mobileFlag = false;
            }
        }
        String p = phoneText.getText();
        if (!p.equals("")) {
            try {
                phone = Integer.parseInt(p);
                if (ver.verifyPhoneNumber(phoneText)) {
                    phoneFlag = true;
                }
            } catch (NumberFormatException e) {
                phoneFlag = false;
            }
        }
        String n = nicNoText.getText();
        if (!n.equals("")) {
            try {
                nic = Integer.parseInt(p);
                if (ver.verifyNICNumber(nicNoText)) {
                    nicNoFlag = true;
                }
            } catch (NumberFormatException e) {
                nicNoFlag = false;
            }
        }
        if (addedDateText.getDate() != null) {
            Date d = addedDateText.getDate();
            addedDate = dateFormat.format(d);
            if (ver.verifyDate(addedDate)) {
                addedDateFlag = true;
            }
        }

        c = new Employee(idLabel.getText(), name, address, mobile, phone, nic, addedDate);
        return c;
    }
