    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        mode = 2;
        clearFieldsPanel();
        setFieldsPanel(false);
        int row = empTable.getSelectedRow();

        idLabel.setText(empTable.getValueAt(row, 0) + "");
        nameText.setText(empTable.getValueAt(row, 1) + "");
        addressText.setText(empTable.getValueAt(row, 2) + "");
        mobileText.setText(empTable.getValueAt(row, 3) + "");
        phoneText.setText(empTable.getValueAt(row, 4) + "");
        nicNoText.setText(empTable.getValueAt(row, 5) + "");
        String d = (String) empTable.getValueAt(row, 6);
        try {
            addedDateText.setDate(dateFormat.parse(d));
        } catch (ParseException ex) {
            Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
        }

        setFieldsPanel(true);
        nameText.selectAll();
        nameText.requestFocus();
    }//GEN-LAST:event_editButtonActionPerformed

